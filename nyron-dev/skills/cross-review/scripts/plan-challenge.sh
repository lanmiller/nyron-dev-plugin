#!/usr/bin/env bash
# plan-challenge.sh — челлендж спеки/плана другой моделью (GPT через codex CLI)
# ДО начала имплементации. Пара к Fable-штурму: Fable ищет узкие места мышлением,
# Sol — сверкой плана с РЕАЛЬНЫМ кодом репо (read-only).
#
# Использование:
#   plan-challenge.sh -C <repo-dir> -t <файл-спеки/плана> [-m <model>]
#
#   -C  каталог репо главной зоны изменений (worktree/чекаут, read-only)
#   -t  файл: спека/план/раскадровка (JTBD, DoD, нарезка, карта изменений)
#   -m  модель (default gpt-5.6-sol, авто-фолбэк без Pro)
#
# Выход: первая строка строго «ПЛАН: ОК» или «ПЛАН: РИСКИ», далее пункты.
set -euo pipefail

REPO="" MODEL="" PLAN_FILE=""
while getopts "C:t:m:" opt; do
  case $opt in
    C) REPO=$OPTARG ;;
    t) PLAN_FILE=$OPTARG ;;
    m) MODEL=$OPTARG ;;
    *) echo "usage: $0 -C <repo> -t <plan-file> [-m model]" >&2; exit 2 ;;
  esac
done
[ -n "$REPO" ] && [ -n "$PLAN_FILE" ] && [ -f "$PLAN_FILE" ] || { echo "ошибка: нужны -C <repo> и -t <файл плана>" >&2; exit 2; }
command -v codex >/dev/null || { echo "ошибка: codex CLI не установлен" >&2; exit 3; }

PROMPT_FILE=$(mktemp)
trap 'rm -f "$PROMPT_FILE"' EXIT
cat > "$PROMPT_FILE" <<EOF
Ты — независимый архитектурный челленджер. План писала ДРУГАЯ модель (Claude);
твоя ценность — проверить его против РЕАЛЬНОГО кода репо, к которому у тебя
есть доступ на чтение. Имплементация ещё НЕ началась: поймать проблему сейчас
стоит копейки, в мерже — вечер.

ПЕРЕД оценкой прочитай каноны проекта: AGENTS.md и/или CLAUDE.md в корне
рабочего каталога (и в подпапках зоны изменений).

Спека/план на челлендж:
$(cat "$PLAN_FILE")

Проверь по убыванию важности, СВЕРЯЯСЬ С КОДОМ (открывай файлы, названные в
плане, и их соседей):
1. Ложные предпосылки: план ссылается на файлы/функции/поля/эндпоинты, которых
   нет или которые ведут себя иначе, чем план предполагает.
2. Легаси-путь: есть готовый механизм, который план дублирует новым (канон
   проекта: дорабатывай легаси, не строй параллель).
3. Узкие места архитектуры: гонки, состояние между сервисами, миграции данных,
   обратная совместимость, флаги (флоу-правки обязаны быть за env-флагом).
4. Нарезка: куски плана пересекаются по файлам (кандидат на конфликт мержа);
   зависимость между кусками не выписана.
5. Дыры скоупа: краевые случаи и сценарии из DoD, которые план не покрывает.
6. Более простой путь: если план можно ужать без потери DoD — покажи как.
Вкусовщину и переименования не писать.

Формат ответа (по-русски, строго):
Первая строка: «ПЛАН: ОК» или «ПЛАН: РИСКИ».
При РИСКАХ — нумерованный список: категория (1-6) — что не так — пруф из кода
(файл:строка/символ) — чем грозит — как поправить план (одной-двумя строками).
При ОК — 2-4 строки: что именно сверил с кодом.
Блок «НЕБЛОКИРУЮЩЕЕ:» — наблюдения, не требующие правки плана.
EOF

OUT_FILE=$(mktemp)
[ -n "$MODEL" ] || MODEL="gpt-5.6-sol"
run_codex() {
  codex exec --sandbox read-only --cd "$REPO" --skip-git-repo-check \
    -c 'model_reasoning_effort="high"' \
    --output-last-message "$OUT_FILE" "$@" - < "$PROMPT_FILE" >&2
}
if ! run_codex -m "$MODEL"; then
  echo "plan-challenge: модель $MODEL недоступна — фолбэк на дефолт codex" >&2
  run_codex
fi
cat "$OUT_FILE"
rm -f "$OUT_FILE"
